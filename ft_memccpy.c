#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)src)[i] == (unsigned char)c)
		{
			((unsigned char *)dest)[0] = ((const unsigned char *)src)[i];
			return (++dest);
		}
		((unsigned char *)dest)[0] = ((unsigned char *)src)[i++];
		dest++;
	}
	return (NULL);
}
